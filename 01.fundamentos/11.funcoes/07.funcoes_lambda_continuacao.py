# Função de potência de um número
power = lambda num: num ** 2
print(power(5))

# Função que verifica se o número é par
is_even = lambda x: x % 2 == 0
print(is_even(3))

# Funçao que divide um número por outro
div_num = lambda x, y: x / y
print(div_num(10, 2))

# Função para reverter string
revert_string = lambda s: s[::-1]
print(revert_string("Python"))